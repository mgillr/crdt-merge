"""
dbt Package Generator — generate cross-database dbt macros for CRDT merge.

Produces Jinja-templated SQL macros that implement CRDT merge strategies
(LWW, Max, Min, Union, Concat, Priority) across Snowflake, BigQuery,
Postgres, and DuckDB.  Users add the generated macros to their dbt project
and call them from models.

Usage::

    from crdt_merge.accelerators.dbt_package import DbtMergeGenerator

    gen = DbtMergeGenerator()
    sql = gen.generate_macro(
        sources=["stg_east", "stg_west"],
        key="customer_id",
        strategies={"name": "lww", "revenue": "max"},
    )
    yaml = gen.generate_packages_yml()

All external dependencies are lazy-imported.
"""

